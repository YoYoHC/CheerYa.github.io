/* 根据维度 id 查营养素对象（兼容原来的二维访问方式） */
function getNutrients(dim){
  // 图片命名规则：
  // - 跨维度共享（egothioneine / pqq / piperine）：n_<short_id>.png
  //   例：n_egothioneine.png，氧化和脑力维度都读这同一张
  // - 单一维度：n_<short_id>.png 同样规则
  //   例：n_akg.png、n_opc.png、n_curcumin.png、n_complex.png
  return (NUTRIENTS[dim] || [])
    .map(id => {
      const o = NUTRIENTS_BY_ID[id];
      if (!o) return null;
      const shortId = id.replace(/^n_/, ""); // n_egothioneine -> egothioneine
      // 用户上传的图实际是 n_<shortId>.png 格式（带 n_ 前缀）
      return Object.assign({ id, dim, img: "n_" + shortId + ".png" }, o);
    })
    .filter(Boolean)
    .filter(n => n.name);
}

